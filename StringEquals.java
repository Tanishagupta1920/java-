public class StringEquals {
    public static void main(String[] args) {
        String s1="Deepak";
        String s2="deepak";
        String s3="Sandhya";
        System.out.println(s1.equals(s2));
        System.out.println(s1.equals(s3));
        System.out.println(s1.equalsIgnoreCase(s2));

    }
    
}
