public class StringCompare {
    public static void main(String[] args) {
        String s1="Deepak";
        String s2="deepak";
        String s3="Sandhya";
        System.out.println(s1.compareTo(s2));
        System.out.println(s1.compareTo(s3));
        System.out.println(s1.compareToIgnoreCase(s2));

    }
    
}
